    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String code = txtCode.getText();
        String nama = txtNama.getText();
        String deksripsi = txtDeskripsi.getText();
        String solusi = txtSolusi.getText();

        // Buat instansi CriteriaModel
        PenyakitModel penyakit = new PenyakitModel();
        penyakit.setCode(code);
        penyakit.setName(nama);
        penyakit.setDescription(deksripsi);
        penyakit.setSolution(solusi);

        // Coba buat kriteria dalam database atau sumber data
        int result = penyakitDao.create(penyakit);

        // Periksa apakah pembuatan berhasil
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
            getAllData(); // Segarkan atau ambil data yang diperbarui
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menambahkan data.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Mendapatkan indeks baris yang dipilih
        int selectedRow = jTable1.getSelectedRow();

        // Memeriksa apakah ada baris yang dipilih
        if (selectedRow != -1) {
            // Mengasumsikan ID berada di kolom pertama (indeks 0) dari tabel
            int id = (int) jTable1.getValueAt(selectedRow, 0);

            // Memanggil metode delete dengan ID yang dipilih
            int result = penyakitDao.delete(id); // Hapus record dengan ID yang dipilih

            // Menampilkan pesan hasil
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                // Opsional, muat ulang data tabel setelah penghapusan
                getAllData(); // Mengasumsikan metode ini memuat ulang data ke dalam tabel
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data.");
            }
        } else {
            // Jika tidak ada baris yang dipilih, tampilkan pesan
            JOptionPane.showMessageDialog(this, "Silakan pilih baris untuk menghapus.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            String templateName = "LaporanDataKriteriaBiznet.jrxml";
            InputStream reportStream = ReportView.class.getResourceAsStream("/resources/reports/" + templateName);
            JasperDesign jd = JRXmlLoader.load(reportStream);

            Connection dbConnection = DatabaseUtil.getInstance().getConnection();

            JasperReport jr = JasperCompileManager.compileReport(jd);

            HashMap parameter = new HashMap();
            parameter.put("PATH","src/resources/images/");
            
            JasperPrint jp = JasperFillManager.fillReport(jr,parameter, dbConnection);
            JasperViewer.viewReport(jp, false);
        } catch (JRException ex) {
            Logger.getLogger(ReportView.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_jButton3ActionPerformed


