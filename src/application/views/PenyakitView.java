     public void getAllData() {
        List<PenyakitModel> penyakitList = penyakitDao.findAll();
        
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"Id", "Code", "Nama", "Deksripsi", "Solusi"}); // Adjust column names as needed

        // Populate the model with data from spareparts
        for (PenyakitModel penyakit : penyakitList) {
            model.addRow(new Object[]{
                penyakit.getId(), 
                penyakit.getCode(), 
                penyakit.getName(), 
                penyakit.getDescription(),
                penyakit.getSolution()
             }
            ); // Add more attributes as needed
        }
        
        // Set the table model to jTable1
        jTable1.setModel(model);
    }

